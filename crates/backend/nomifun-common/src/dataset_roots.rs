//! Canonical registry for product-owned roots below the application data dir.
//!
//! Factory reset owns every entry in this registry. Portable backup includes
//! only entries explicitly marked [`BackupPolicy::Include`]; every exclusion
//! carries a stable reason so a manifest can prove its coverage rather than
//! silently omitting an unknown root.

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum DatasetRootKind {
    File,
    Directory,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum BackupPolicy {
    Include,
    Exclude(&'static str),
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct ManagedDatasetRoot {
    pub path: &'static str,
    pub kind: DatasetRootKind,
    pub backup: BackupPolicy,
}

const REGENERATED: &str = "regenerated by the current v3 application";
const RUNTIME_ONLY: &str = "runtime or cache data is not portable";
